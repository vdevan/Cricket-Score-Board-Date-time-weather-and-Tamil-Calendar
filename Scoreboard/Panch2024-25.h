String GetTC(uint16_t days)
{
	TC temp = TCal[days];
	bool bdouble = false;
	String mnth = TMonth[temp.Tmonth];
	mnth.trim();
	String th1 = Tithi[temp.Tithi1];
	th1.trim();
	String cal =  mnth + " " + String(temp.Tday) + " "; 
	bWaxing = temp.Paksha;
	if (temp.Tithi1 != temp.Tithi2)
	{
		String th2 = Tithi[temp.Tithi2];
		th2.trim();
		cal += th1 + String("/") + th2 + " ";
		bdouble = true;
	}
	else
		cal += th1 +  String(" ");

	if (temp.Nakshatra1 != temp.Nakshatra2)
	{
		cal += Nakshtra[temp.Nakshatra1] + String("/") + Nakshtra[temp.Nakshatra2];
		bdouble = true;
	}
	else
		cal += Nakshtra[temp.Nakshatra1];

	if (!bdouble)
		cal = Varsha[temp.Tyear] + String(" ") + cal;

	return cal;
}